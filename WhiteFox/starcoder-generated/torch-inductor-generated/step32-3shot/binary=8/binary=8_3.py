
class Model(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.conv = torch.nn.Conv2d(3, 8, 1, stride=1, padding=1)
 
    def forward(self, x1, x2=None):
        res = self.conv(x1)
        res_add = res + x2 # add x2 to the res variable
        return res_add

