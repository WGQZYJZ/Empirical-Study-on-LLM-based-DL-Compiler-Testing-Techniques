
class Model(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.linear = torch.nn.Linear(64, 64)
 
    def forward(self, x1):
        v3 = self.linear(x1)
        v4 = torch.clamp(v3+3, min=0, max=6)
        v5 = v4 / 6
        return v5

# Initializing the model
m = Model()

