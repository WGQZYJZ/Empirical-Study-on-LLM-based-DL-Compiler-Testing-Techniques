
class Model(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.linear = torch.nn.Linear(3, 3)
 
    def forward(self, x1, other=None):
        y = self.linear(x1)
        if not other is None:
            y = y + other
        y = torch.relu(y)
        return y

