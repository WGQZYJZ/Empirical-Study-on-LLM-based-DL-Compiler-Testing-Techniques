
class Model(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.conv1 = torch.nn.Conv2d(3, 8, 1, stride=1, padding=1)
        self.conv2 = torch.nn.Conv2d(3, 8, 1, stride=1, padding=1)
        self.conv3 = torch.nn.ConvTranspose2d(3, 16, 2, stride=2, padding=0, output_padding=0, groups=1, bias=False, dilation=1)
    def forward(self, x1, x2):
        v1 = self.conv1(x1)
        v2 = self.conv2(x2)
        v3 = torch.relu(v1 + v2)
        v4 = self.conv3(v3)
        return v4
