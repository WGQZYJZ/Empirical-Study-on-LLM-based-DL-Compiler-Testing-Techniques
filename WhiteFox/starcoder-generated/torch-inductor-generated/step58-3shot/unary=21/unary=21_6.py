
class ModelTanh(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.conv1 = torch.nn.Conv2d(3, 3, 1)
        self.conv2 = torch.nn.Conv2d(28, 25, 1)
        self.conv3 = torch.nn.Conv2d(25, 24, 1)
        self.conv4 = torch.nn.Conv2d(24, 24, 1)
        self.conv5 = torch.nn.Conv2d(24, 24, 1)
        self.conv6 = torch.nn.Conv2d(24, 23, 1)
        self.conv7 = torch.nn.Conv2d(22, 21, 1)
        self.conv8 = torch.nn.Conv2d(21, 19, 1)
        self.conv9 = torch.nn.Conv2d(19, 19, 1)
        self.conv10 = torch.nn.Conv2d(19, 2, 1)
    def forward(self, x):
        v1 = self.conv1(x)
        v2 = torch.tanh(v1)
        v3 = self.conv2(x)
        v4 = torch.tanh(v3)
        v5 = torch.add(v2, v4)
        v6 = self.conv3(v5)
        v7 = torch.tanh(v6)
        v8 = self.conv4(v7)
        v9 = torch.tanh(v8)
        v10 = torch.add(v5, v9)
        v11 = self.conv5(v10)
        v12 = torch.tanh(v11)
        v13 = torch.add(v6, v12)
        v14 = self.conv6(v13)
        v15 = torch.tanh(v14)
        v16 = torch.add(v10, v15)
        v17 = self.conv7(v16)
        v18 = torch.tanh(v17)
        v19 = torch.add(v13, v18)
        v20 = self.conv8(v19)
        v21 = torch.tanh(v20)
        v22 = torch.add(v16, v21)
        v23 = self.conv9(v22)
        v24 = torch.tanh(v23)
        v25 = torch.add(v19, v24)
        v26 = self.conv10(v25)
        v27 = torch.tanh(v26)
        return v27
