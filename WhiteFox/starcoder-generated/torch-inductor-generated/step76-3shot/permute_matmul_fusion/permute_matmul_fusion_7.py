
class Model(torch.nn.Module):
    def __init__(self):
        super().__init__()
    def forward(self, x1, x2):
        v0 = x1.permute(0, 1, 2)
        v2 = x2.permute(2, 0, 1)
        v3 = torch.bmm(v0, v2)
        return v3
